class ClaudeAPIError(RuntimeError):
    """Raised when the Claude API returns an error response."""


def _claude_error_message(status: int, body: Any) -> str:
    """Return a user-friendly error message for a Claude API HTTP error."""
    # Include the actual error body for debugging
    detail = ""
    if body:
        try:
            detail = f" Response: {json.dumps(body)[:500]}"
        except (TypeError, ValueError):
            detail = f" Response: {str(body)[:500]}"

    if status in (401, 403):
        return (
            f"Claude API authentication failed (HTTP {status}) — "
            "check your Claude credentials (`CLAUDE_API_URL` / `CLAUDE_API_TOKEN`, "
            "or `HAWKEYE_CLAUDE_API_URL` / `HAWKEYE_CLAUDE_BLOB` when set via the webhook server)."
            f"{detail}"
        )
    if status == 429:
        return f"Claude API rate limit exceeded — please wait a moment and retry.{detail}"
    if status >= 500:
        return f"Claude service error (HTTP {status}) — this is likely transient, please retry.{detail}"
    return f"Claude API error (HTTP {status}) — check your endpoint configuration.{detail}"


def call_claude(prompt: str, api_url: str, api_token: str, *, timeout: int = 180, max_tokens: int = 16384) -> str:
    headers = {
        "Content-Type": "application/json",
        "Authorization": f"Bearer {api_token}",
    }
    payload = {
        "messages": [
            {"role": "user", "content": [{"text": prompt}]},
        ],
        "inferenceConfig": {"maxTokens": max_tokens},
    }
    result = http_post(api_url, headers, payload, timeout=timeout)
    if result["status"] >= 400:
        raise ClaudeAPIError(_claude_error_message(result["status"], result["body"]))
    data = result["body"]

    stop_reason = data.get("stopReason", "")
    if stop_reason == "max_tokens":
        print("  WARNING: Response truncated (hit maxTokens limit).", file=sys.stderr)

    try:
        contents = (data.get("output") or {}).get("message", {}).get("content", [])
        for c in contents:
            if isinstance(c, dict) and c.get("text"):
                return c["text"]
        print("  WARNING: Claude response contained no text content blocks.", file=sys.stderr)
    except Exception as exc:
        print(f"  WARNING: unexpected Claude response structure: {exc}", file=sys.stderr)

    return json.dumps(data)


def _extract_json_block(text: str) -> str:
    """Extract the JSON object from Claude's response, handling prose and fences.

    Handles these common patterns:
      1. Clean JSON: ``{ ... }``
      2. Fenced JSON: ``\\`\\`\\`json\\n{ ... }\\n\\`\\`\\````
      3. Prose + fenced JSON: ``## Review\\n\\`\\`\\`json\\n{ ... }\\n\\`\\`\\````
      4. Trailing commas: ``{ "a": 1, }``
    """
    # Try to find a fenced JSON block first (handles prose before the fence)
    fence_match = re.search(r"```(?:json)?\s*\n(\{.*?)\n```", text, re.DOTALL)
    if fence_match:
        block = fence_match.group(1).strip()
    else:
        # No fence — find the first { and last }
        start = text.find("{")
        end = text.rfind("}")
        if start != -1 and end > start:
            block = text[start:end + 1]
        else:
            block = text.strip()

    # Fix trailing commas before } or ] (common Claude JSON error)
    block = re.sub(r",\s*([}\]])", r"\1", block)
    return block


def parse_response(text: str) -> dict[str, Any]:
    """Parse Claude's JSON response, tolerating prose wrappers and minor JSON errors."""
    cleaned = _extract_json_block(text)
    try:
        result = json.loads(cleaned)
    except json.JSONDecodeError as exc:
        print(f"JSON parse error: {exc}", file=sys.stderr)
        print(f"Cleaned JSON (first 500 chars): {cleaned[:500]}", file=sys.stderr)
        print(f"Original text (first 500 chars): {text.strip()[:500]}", file=sys.stderr)
        return {}
    if not isinstance(result, dict):
        print(f"Expected JSON object, got {type(result).__name__}", file=sys.stderr)
        return {}
    return result


