def main() -> None:
    if len(sys.argv) < 4:
        print(
            "Usage: hawkeye_pr_review.py <owner> <repo> <pr_number>",
            file=sys.stderr,
        )
        sys.exit(1)

    owner, repo, pr_str = sys.argv[1:4]
    pr_number = int(pr_str)

    github_token = os.environ.get("GITHUB_TOKEN")
    if not github_token:
        print("Missing required environment variable: GITHUB_TOKEN", file=sys.stderr)
        sys.exit(1)

    pr_info = get_pr_info(owner, repo, pr_number, github_token)
    head_sha = pr_info["head"]["sha"]

    api_url = os.environ.get("CLAUDE_API_URL")
    api_token = os.environ.get("CLAUDE_API_TOKEN")

    missing = []
    if not api_url:
        missing.append("CLAUDE_API_URL")
    if not api_token:
        missing.append("CLAUDE_API_TOKEN")
    if missing:
        print(f"Missing required environment variables: {', '.join(missing)}", file=sys.stderr)
        sys.exit(1)

    model_name = get_model_name(api_url)
    print(f"Reviewing PR #{pr_number} on {owner}/{repo} with {model_name}...", file=sys.stderr)

    set_commit_status(owner, repo, head_sha, "pending", f"{model_name} is reviewing this PR...", github_token)

    placeholder_id_str = os.environ.get("PLACEHOLDER_COMMENT_ID", "")
    try:
        placeholder_id = int(placeholder_id_str) if placeholder_id_str else None
    except ValueError:
        print(f"Invalid PLACEHOLDER_COMMENT_ID: {placeholder_id_str!r}", file=sys.stderr)
        placeholder_id = None

    logo = f'<img src="{BOT_AVATAR}" width="18" height="18" align="absmiddle">'
    footer_logo = f'<img src="{BOT_AVATAR}" width="13" height="13" align="absmiddle">'

    _enable_file_content_cache()
    try:
        files = get_changed_files(owner, repo, pr_number, github_token)
        print(f"  {len(files)} changed file(s).", file=sys.stderr)

        skipped_by_rule: list[str] = []
        reviewable_files: list[dict] = []
        for f in files:
            reason = _should_skip_file(f["filename"])
            if reason:
                skipped_by_rule.append(f"{f['filename']} ({reason})")
            else:
                reviewable_files.append(f)
        if skipped_by_rule:
            print(
                f"  Skipped {len(skipped_by_rule)} file(s): {'; '.join(skipped_by_rule)}",
                file=sys.stderr,
            )
        files = reviewable_files

        valid_lines: dict[str, set[int]] = {}
        for f in files:
            valid_lines[f["filename"]] = get_diff_lines(f.get("patch", ""))

        repo_tree, tree_paths = get_repo_tree(owner, repo, head_sha, github_token, files)

        total_changes = sum(f.get("changes", 0) for f in files)
        reviewable_count = sum(1 for f in files if f.get("status") != "removed")

        if reviewable_count == 0:
            has_removals = any(f.get("status") == "removed" for f in files)
            if skipped_by_rule and not has_removals and not files:
                overview = (
                    f"All changed files were filtered out ({len(skipped_by_rule)} skipped). "
                    "No reviewable source code to analyze."
                )
            elif skipped_by_rule and has_removals:
                overview = (
                    f"This PR removes files and {len(skipped_by_rule)} additional "
                    "file(s) were filtered out — no reviewable source code to analyze."
                )
            else:
                overview = "This PR only removes files — no code to review."
            print(f"  No reviewable files — skipping review.", file=sys.stderr)
            summary_body = format_summary_comment(
                {"overview": overview},
                [], model_name,
            )
            post_review(owner, repo, pr_number, head_sha, summary_body, [], github_token, placeholder_id)
            set_commit_status(owner, repo, head_sha, "success", "No reviewable files", github_token)
            return

        use_map_reduce = (
            reviewable_count >= MAP_REDUCE_MIN_FILES
            or total_changes >= MAP_REDUCE_MIN_CHANGES
        )

        review_mode = ""
        failed_b = 0
        failed_files: list[str] = []
        file_coverage: dict[str, str] = {}

        if use_map_reduce:
            batches = group_files_into_batches(files)
            num_batches = len(batches)
            print(
                f"  Using map-reduce review ({reviewable_count} files, "
                f"{total_changes} changes, {num_batches} batches)...",
                file=sys.stderr,
            )
            summary, all_returned, failed_b, failed_files, file_coverage = review_map_reduce(
                pr_info, files, owner, repo, head_sha, github_token,
                api_url, api_token, repo_tree, tree_paths,
                batches=batches,
                placeholder_comment_id=placeholder_id,
            )
            if failed_b:
                review_mode = f"Map-reduce review ({num_batches} batches, {failed_b} failed)"
            else:
                review_mode = f"Map-reduce review ({num_batches} batches)"
        else:
            prompt = build_prompt(
                pr_info, files, owner, repo, head_sha, github_token,
                repo_tree=repo_tree, tree_paths=tree_paths,
            )

            print("  Calling Claude...", file=sys.stderr)
            claude_text = call_claude(prompt, api_url, api_token)

            response = parse_response(claude_text)
            if not response:
                print("  Could not parse Claude response; posting raw text as summary.", file=sys.stderr)
                fallback_body = (
                    f"<h2>{logo} HawkEye Review</h2>\n\n"
                    "Claude returned a response that could not be parsed as structured JSON.\n\n"
                    f"<details><summary>Raw response</summary>\n\n```\n{claude_text[:4000]}\n```\n</details>\n\n"
                    f"<sub>{footer_logo} Reviewed by **{model_name}** (Anthropic) via Amazon Bedrock | v{VERSION}</sub>\n\n"
                    f"<sub>{AI_DISCLAIMER}</sub>"
                )
                post_review(owner, repo, pr_number, head_sha, fallback_body, [], github_token, placeholder_id)
                set_commit_status(owner, repo, head_sha, "success", "Review complete", github_token)
                return

            summary = response.get("summary", {})
            all_returned = response.get("comments", [])

        raw_comments = [
            c
            for c in all_returned
            if isinstance(c, dict) and c.get("path") and isinstance(c.get("line"), int) and c.get("message")
        ]
        print(
            f"  {len(all_returned)} comment(s) returned, "
            f"{len(raw_comments)} valid after schema check.",
            file=sys.stderr,
        )

        existing = get_existing_review_comments(owner, repo, pr_number, github_token)
        if existing:
            print(f"  Found {len(existing)} existing review comment(s) for dedup.", file=sys.stderr)
        comments = filter_comments(raw_comments, valid_lines, existing)
        print(f"  {len(comments)} comment(s) survived filtering.", file=sys.stderr)

        total_additions = sum(f.get("additions", 0) for f in files)
        if not comments and total_additions >= MIN_ADDITIONS_FOR_RETRY:
            print(
                f"  Zero comments on {total_additions} additions — running second-pass review...",
                file=sys.stderr,
            )
            retry_related = get_related_context(files, owner, repo, head_sha, github_token, tree_paths)
            retry_linter_config = get_linter_config(owner, repo, head_sha, github_token, tree_paths)
            retry_linter_section = (
                "## Linter and formatter configuration\n\n"
                "The following linter/formatter configs are active in this project. "
                "All suggested code changes MUST comply with these rules.\n\n"
                f"{retry_linter_config}"
            ) if retry_linter_config else ""
            retry_prompt = build_retry_prompt(pr_info, files, retry_related, retry_linter_section)
            retry_text = call_claude(retry_prompt, api_url, api_token)
            retry_response = parse_response(retry_text)
            if retry_response:
                retry_all = retry_response.get("comments", [])
                retry_raw = [
                    c
                    for c in retry_all
                    if isinstance(c, dict) and c.get("path") and isinstance(c.get("line"), int) and c.get("message")
                ]
                print(
                    f"  Second pass returned {len(retry_all)} comment(s), "
                    f"{len(retry_raw)} valid.",
                    file=sys.stderr,
                )
                retry_comments = filter_comments(retry_raw, valid_lines, existing)
                print(f"  {len(retry_comments)} second-pass comment(s) survived filtering.", file=sys.stderr)
                comments.extend(retry_comments)

        is_partial = use_map_reduce and failed_b > 0
        summary_body = format_summary_comment(
            summary, comments, model_name, review_mode,
            partial=is_partial, failed_files=failed_files if is_partial else None,
            file_coverage=file_coverage if file_coverage else None,
        )
        post_review(owner, repo, pr_number, head_sha, summary_body, comments, github_token, placeholder_id)

        if use_map_reduce and failed_b:
            set_commit_status(
                owner, repo, head_sha, "failure",
                f"Partial review — {failed_b}/{num_batches} batches failed, comment @hawkeye review to retry",
                github_token,
            )
        else:
            set_commit_status(owner, repo, head_sha, "success", "Review complete", github_token)

        print("Done.", file=sys.stderr)

    except ClaudeAPIError as exc:
        print(f"  Claude API error: {exc}", file=sys.stderr)
        error_body = (
            f"<h2>{logo} HawkEye Review</h2>\n\n"
            f"\u274c **Claude API error:** {exc}\n\n"
            "Comment `@hawkeye review` to retry.\n\n"
            f"<sub>{footer_logo} Reviewed by **{model_name}** (Anthropic) via Amazon Bedrock | v{VERSION}</sub>\n\n"
            f"<sub>{AI_DISCLAIMER}</sub>"
        )
        if placeholder_id:
            try:
                edit_comment(owner, repo, placeholder_id, error_body, github_token)
            except Exception as cleanup_exc:
                print(f"  Placeholder update failed: {cleanup_exc}", file=sys.stderr)
        try:
            set_commit_status(owner, repo, head_sha, "error", str(exc), github_token)
        except Exception as status_exc:
            print(f"  Status update failed: {status_exc}", file=sys.stderr)
        sys.exit(1)
    except Exception as exc:
        print(f"  Review failed: {exc}", file=sys.stderr)
        error_body = (
            f"<h2>{logo} HawkEye Review</h2>\n\n"
            f"\u274c Review failed: `{type(exc).__name__}: {exc}`\n\n"
            "This may be a transient issue. Comment `@hawkeye review` to retry.\n\n"
            f"<sub>{footer_logo} Reviewed by **{model_name}** (Anthropic) via Amazon Bedrock | v{VERSION}</sub>\n\n"
            f"<sub>{AI_DISCLAIMER}</sub>"
        )
        if placeholder_id:
            try:
                edit_comment(owner, repo, placeholder_id, error_body, github_token)
            except Exception as cleanup_exc:
                print(f"  Placeholder update failed: {cleanup_exc}", file=sys.stderr)
        try:
            set_commit_status(owner, repo, head_sha, "error", "Review failed — comment @hawkeye review to retry", github_token)
        except Exception as status_exc:
            print(f"  Status update failed: {status_exc}", file=sys.stderr)
        sys.exit(1)
    finally:
        _disable_file_content_cache()


if __name__ == "__main__":
    main()
