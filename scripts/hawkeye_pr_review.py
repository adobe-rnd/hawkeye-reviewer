COVERAGE_ICONS = {
    "full": "\u2705",
    "diff-only": "\U0001f50d",
    "skipped": "\u26a0\ufe0f",
}


def format_summary_comment(
    summary: dict,
    comments: list[dict],
    model_name: str = "Claude",
    review_mode: str = "",
    partial: bool = False,
    failed_files: list[str] | None = None,
    file_coverage: dict[str, str] | None = None,
) -> str:
    parts: list[str] = []

    logo = f'<img src="{BOT_AVATAR}" width="18" height="18" align="absmiddle">'

    header = f"<h2>{logo} Pull request overview</h2>"
    overview = summary.get("overview", "")
    if overview:
        parts.append(f"{header}\n\n{overview}")
    else:
        parts.append(header)

    changes = summary.get("changes", [])
    if changes:
        bullets = "\n".join(f"- {c}" for c in changes)
        parts.append(f"**Changes:**\n{bullets}")

    files = summary.get("files", [])
    if files:
        rows = "\n".join(
            f"| `{f.get('path', '')}` | {f.get('description', '')} |" for f in files
        )
        table = "| File | Description |\n|------|-------------|\n" + rows
        parts.append(f"### Reviewed changes\n\n{table}")

    if comments:
        counts: dict[str, int] = {}
        for c in comments:
            sev = c.get("severity", "suggestion")
            counts[sev] = counts.get(sev, 0) + 1
        breakdown = ", ".join(
            f"{SEVERITY_ICONS.get(s, '')} {count} {s}" for s, count in counts.items()
        )
        parts.append(
            f"\n---\n{len(comments)} inline comment{'s' if len(comments) != 1 else ''} "
            f"posted ({breakdown})."
        )
    else:
        parts.append(
            "\n---\n\u2705 No issues found — looks good!"
        )

    if partial:
        msg = (
            "\n\u26a0\ufe0f **Review incomplete** — some batches failed. "
            "Not all files could be analyzed. Comment `@hawkeye review` to retry."
        )
        if failed_files:
            file_list = "\n".join(f"- `{f}`" for f in failed_files)
            msg += (
                f"\n\n<details><summary>Files in failed batches ({len(failed_files)})"
                f"</summary>\n\n{file_list}\n\n</details>"
            )
        parts.append(msg)

    if file_coverage:
        full_count = sum(1 for v in file_coverage.values() if v == "full")
        diff_count = sum(1 for v in file_coverage.values() if v == "diff-only")
        skipped_count = sum(1 for v in file_coverage.values() if v == "skipped")
        total = len(file_coverage)
        coverage_summary = (
            f"\u2705 {full_count} full"
            f" &nbsp;| &nbsp;\U0001f50d {diff_count} diff-only"
            f" &nbsp;| &nbsp;\u26a0\ufe0f {skipped_count} skipped"
            f" &nbsp;| &nbsp;{total} total"
        )
        rows_list: list[str] = []
        for path in sorted(file_coverage.keys()):
            mode = file_coverage[path]
            icon = COVERAGE_ICONS.get(mode, "")
            rows_list.append(f"| `{path}` | {icon} {mode} |")
        coverage_table = "| File | Review depth |\n|------|-------------|\n" + "\n".join(rows_list)
        parts.append(
            f"\n<details><summary>File coverage ({coverage_summary})</summary>\n\n"
            f"{coverage_table}\n\n</details>"
        )

    footer_logo = f'<img src="{BOT_AVATAR}" width="13" height="13" align="absmiddle">'
    mode_text = f" | {review_mode}" if review_mode else ""
    parts.append(
        f"<sub>{footer_logo} Reviewed by **{model_name}** (Anthropic) via Amazon Bedrock{mode_text}"
        f" | Comment `@hawkeye review` to re-review | v{VERSION}</sub>\n\n"
        f"<sub>{AI_DISCLAIMER}</sub>"
    )

    return "\n\n".join(parts)


def format_inline_body(comment: dict) -> str:
    sev = comment.get("severity", "suggestion")
    icon = SEVERITY_ICONS.get(sev, "\U0001f4a1")
    label = SEVERITY_LABELS.get(sev, "Suggestion")
    message = comment.get("message", "")

    body = f"{icon} **{label}**\n\n{message}"

    suggestion = comment.get("suggestion")
    if suggestion:
        body += f"\n\n```suggestion\n{suggestion}\n```"

    body += f"\n\n<sub>{AI_DISCLAIMER}</sub>"

    return body


