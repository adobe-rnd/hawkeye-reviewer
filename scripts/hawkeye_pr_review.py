SMART_FILE_HEADER_LINES = 30
SMART_FILE_CONTEXT_LINES = 40
SMART_FILE_COVERAGE_THRESHOLD = 0.7

_DEFINITION_RE = re.compile(
    r"^[ \t]{0,8}"
    r"(?:(?:export|default|public|private|protected|static|abstract|async|"
    r"override|final|sealed|pub|unsafe)\s+)*"
    r"(?:def|function\*?|class|interface|type|enum|struct|trait|impl|fn|func|module)"
    r"\s+\w+"
)


def _extract_hunk_line_ranges(patch: str) -> list[tuple[int, int]]:
    """Extract (start_line, end_line) on the new-file side for each diff hunk."""
    ranges: list[tuple[int, int]] = []
    for m in re.finditer(
        r"^@@ -\d+(?:,\d+)? \+(\d+)(?:,(\d+))? @@", patch, re.MULTILINE,
    ):
        start = int(m.group(1))
        count = int(m.group(2)) if m.group(2) else 1
        if count > 0:
            ranges.append((start, start + count - 1))
        else:
            ranges.append((start, start))
    return ranges


def _merge_ranges(
    ranges: list[tuple[int, int]], gap: int = 0,
) -> list[tuple[int, int]]:
    """Merge overlapping or adjacent ranges (within *gap* lines of each other)."""
    if not ranges:
        return []
    sorted_ranges = sorted(ranges)
    merged = [sorted_ranges[0]]
    for start, end in sorted_ranges[1:]:
        prev_start, prev_end = merged[-1]
        if start <= prev_end + gap + 1:
            merged[-1] = (prev_start, max(prev_end, end))
        else:
            merged.append((start, end))
    return merged


def _expand_ranges(
    ranges: list[tuple[int, int]], context: int, max_line: int,
) -> list[tuple[int, int]]:
    """Expand each range by *context* lines in both directions and merge."""
    expanded = [
        (max(1, s - context), min(max_line, e + context)) for s, e in ranges
    ]
    return _merge_ranges(expanded)


def _build_smart_file_block(
    path: str, status: str, content: str, patch: str,
) -> str:
    """Build a file block with smart context: full content for small files,
    expanded diff hunks + structural signatures for large files."""
    file_lines = content.splitlines()
    total_lines = len(file_lines)

    if total_lines <= SMART_FILE_THRESHOLD or not patch:
        return _build_file_block(path, status, content, patch)

    hunk_ranges = _extract_hunk_line_ranges(patch)
    if not hunk_ranges:
        return _build_file_block(path, status, content, patch)

    expanded = _expand_ranges(
        hunk_ranges, SMART_FILE_CONTEXT_LINES, total_lines,
    )
    header_range = (1, min(SMART_FILE_HEADER_LINES, total_lines))
    merged = _merge_ranges([header_range] + expanded, gap=5)

    included_count = sum(e - s + 1 for s, e in merged)
    if included_count / total_lines >= SMART_FILE_COVERAGE_THRESHOLD:
        return _build_file_block(path, status, content, patch)

    included: set[int] = set()
    for s, e in merged:
        included.update(range(s, e + 1))

    signatures: list[str] = []
    for i, line in enumerate(file_lines, 1):
        if i not in included and _DEFINITION_RE.match(line):
            signatures.append(f"{i}|{line}")

    sections: list[str] = []
    for s, e in merged:
        chunk = [
            f"{i}|{file_lines[i - 1]}"
            for i in range(s, min(e + 1, total_lines + 1))
        ]
        sections.append("\n".join(chunk))

    numbered = "\n...\n".join(sections)
    if signatures:
        numbered += (
            "\n\n# Other definitions in this file (not shown):\n"
            + "\n".join(signatures)
        )

    diff_section = f"\nDIFF:\n{patch}\n" if patch else ""
    omitted = total_lines - included_count
    return (
        f"FILE: {path} (status: {status}) "
        f"[{total_lines} lines, {omitted} omitted — "
        f"showing imports + context around changes]\n"
        f"{numbered}{diff_section}\n\n"
    )


def fetch_shared_context(
    owner: str,
    repo: str,
    head_sha: str,
    token: str,
    repo_tree: str = "",
    tree_paths: set[str] | None = None,
) -> dict[str, str]:
    """Fetch repo context, docs, guidelines, linter config, and tree formatting — shared across all review batches."""
    with ThreadPoolExecutor(max_workers=4) as ex:
        f_ctx   = ex.submit(get_repo_context,      owner, repo, head_sha, token, tree_paths)
        f_docs  = ex.submit(get_repo_docs,          owner, repo, head_sha, token, tree_paths)
        f_guide = ex.submit(get_review_guidelines,  owner, repo, head_sha, token, tree_paths)
        f_lint  = ex.submit(get_linter_config,      owner, repo, head_sha, token, tree_paths)
        repo_context  = f_ctx.result()
        repo_docs     = f_docs.result()
        guidelines    = f_guide.result()
        linter_config = f_lint.result()

    return {
        "repo_context_section": f"## Repository context\n\n{repo_context}" if repo_context else "",
        "repo_docs_section": f"## Project documentation\n\n{repo_docs}" if repo_docs else "",
        "guidelines_section": f"## Repository guidelines\n\n{guidelines}" if guidelines else "",
        "guidelines_raw": guidelines,
        "linter_config_section": (
            "## Linter and formatter configuration\n\n"
            "The following linter/formatter configs are active in this project. "
            "All suggested code changes MUST comply with these rules.\n\n"
            f"{linter_config}"
        ) if linter_config else "",
        "repo_tree_section": f"## Repository structure\n\n```\n{repo_tree}\n```" if repo_tree else "",
    }


def build_prompt(
    pr_info: dict,
    files: list[dict],
    owner: str,
    repo: str,
    head_sha: str,
    token: str,
    repo_tree: str = "",
    tree_paths: set[str] | None = None,
) -> str:
    included_files: list[str] = []
    skipped_files: list[str] = []
    file_contents: dict[str, str] = {}
    total_chars = 0
    max_chars = 180_000

    for f in files:
        if f.get("status") == "removed":
            continue
        path = f["filename"]
        patch = f.get("patch", "")
        status = f.get("status", "modified")
        changes = f.get("changes", 0)

        if changes > 800:
            if patch:
                block = _build_diff_only_block(path, status, patch)
                if total_chars + len(block) <= max_chars:
                    included_files.append(block)
                    total_chars += len(block)
                    print(f"  {path}: included diff only ({changes} changes, too large for full content)", file=sys.stderr)
                    continue
            skipped_files.append(f"{path} ({changes} changes — exceeded budget even for diff)")
            print(f"  {path}: skipped ({changes} changes, exceeded budget)", file=sys.stderr)
            continue

        content = get_file_content(owner, repo, head_sha, path, token)
        if not content:
            if patch:
                block = _build_diff_only_block(path, status, patch)
                if total_chars + len(block) <= max_chars:
                    included_files.append(block)
                    total_chars += len(block)
                    print(f"  {path}: included diff only (could not fetch content)", file=sys.stderr)
                    continue
            skipped_files.append(f"{path} (could not fetch content)")
            continue

        file_contents[path] = content

        block = _build_smart_file_block(path, status, content, patch)

        if total_chars + len(block) > max_chars:
            if patch:
                fallback = _build_diff_only_block(path, status, patch)
                if total_chars + len(fallback) <= max_chars:
                    included_files.append(fallback)
                    total_chars += len(fallback)
                    print(f"  {path}: included diff only (full content exceeded budget)", file=sys.stderr)
                    continue
            skipped_files.append(f"{path} (exceeded budget)")
            print(f"  {path}: skipped (exceeded budget)", file=sys.stderr)
            continue

        included_files.append(block)
        total_chars += len(block)

    if skipped_files:
        print(f"  {len(skipped_files)} file(s) skipped: {'; '.join(skipped_files)}", file=sys.stderr)

    files_text = "\n".join(included_files) if included_files else "(No reviewable file content.)"

    shared_context = fetch_shared_context(owner, repo, head_sha, token, repo_tree, tree_paths)

    fetched_paths: set[str] = set()

    sibling_context = ""
    if tree_paths:
        sibling_context = get_sibling_files(files, tree_paths, owner, repo, head_sha, token, fetched_paths)
    sibling_section = (
        "## Sibling files (same directories as changed files)\n\n"
        "These files are NOT being changed but exist alongside the changed files. "
        "Use them to verify the changes follow existing patterns and conventions.\n\n"
        f"{sibling_context}"
    ) if sibling_context else ""

    import_context = ""
    if tree_paths and file_contents:
        changed_paths = {f["filename"] for f in files}
        import_context = get_imported_files(file_contents, tree_paths, changed_paths, owner, repo, head_sha, token, fetched_paths)
    import_section = (
        "## Imported local modules (referenced by changed code)\n\n"
        "These local project files are imported by the changed files. "
        "Use them to verify correct API usage and interface compliance.\n\n"
        f"{import_context}"
    ) if import_context else ""

    related_context = get_related_context(files, owner, repo, head_sha, token, tree_paths, fetched_paths)
    related_context_section = (
        f"## Related context files (not part of this PR)\n\n"
        f"These files are NOT being changed but may be affected by or relevant to the changes above.\n\n"
        f"{related_context}"
    ) if related_context else ""

    return REVIEW_PROMPT.format(
        title=pr_info.get("title", ""),
        description=(pr_info.get("body") or "(no description)")[:2000],
        repo_context_section=shared_context["repo_context_section"],
        repo_docs_section=shared_context["repo_docs_section"],
        guidelines_section=shared_context["guidelines_section"],
        linter_config_section=shared_context.get("linter_config_section", ""),
        repo_tree_section=shared_context["repo_tree_section"],
        sibling_section=sibling_section,
        import_section=import_section,
        related_context_section=related_context_section,
        files_text=files_text,
    )


