def _file_budget(f: dict) -> int:
    """Estimate how many prompt chars a file will consume in a batch."""
    patch_len = len(f.get("patch", ""))
    changes = f.get("changes", 0)
    # For files ≤800 changes we include smart file content; estimate ~3x patch size.
    # For larger files we include diff only.
    if changes > 800:
        return patch_len + 200
    return max(patch_len * 3, changes * 80) + 200


def group_files_into_batches(files: list[dict]) -> list[list[dict]]:
    """Group changed files into batches by directory proximity and size budget.

    Keeps files from the same directory together. Each batch respects both
    a file-count cap (BATCH_MAX_FILES) and a char-budget cap (BATCH_MAX_CHARS)
    to prevent oversized batches that silently skip files.
    """
    reviewable = [f for f in files if f.get("status") != "removed"]
    if not reviewable:
        return []

    dir_groups: dict[str, list[dict]] = {}
    for f in reviewable:
        d = os.path.dirname(f["filename"])
        dir_groups.setdefault(d, []).append(f)

    def _split_group(group: list[dict]) -> list[list[dict]]:
        """Split a directory group into sub-batches respecting both caps."""
        chunks: list[list[dict]] = []
        chunk: list[dict] = []
        chunk_chars = 0
        for f in group:
            cost = _file_budget(f)
            if chunk and (len(chunk) >= BATCH_MAX_FILES or chunk_chars + cost > BATCH_MAX_CHARS):
                chunks.append(chunk)
                chunk = []
                chunk_chars = 0
            chunk.append(f)
            chunk_chars += cost
        if chunk:
            chunks.append(chunk)
        return chunks

    batches: list[list[dict]] = []
    current_batch: list[dict] = []
    current_chars = 0

    for d in sorted(dir_groups.keys()):
        group = dir_groups[d]
        group_chars = sum(_file_budget(f) for f in group)

        # Large directory: flush current batch, then split the group
        if len(group) > BATCH_MAX_FILES or group_chars > BATCH_MAX_CHARS:
            if current_batch:
                batches.append(current_batch)
                current_batch = []
                current_chars = 0
            batches.extend(_split_group(group))
            continue

        # Would exceed caps: flush current batch first
        if current_batch and (
            len(current_batch) + len(group) > BATCH_MAX_FILES
            or current_chars + group_chars > BATCH_MAX_CHARS
        ):
            batches.append(current_batch)
            current_batch = []
            current_chars = 0

        current_batch.extend(group)
        current_chars += group_chars

    if current_batch:
        batches.append(current_batch)

    return batches


def build_batch_prompt(
    pr_info: dict,
    batch_files: list[dict],
    all_files: list[dict],
    batch_num: int,
    total_batches: int,
    owner: str,
    repo: str,
    head_sha: str,
    token: str,
    shared_context: dict[str, str],
    tree_paths: set[str] | None,
    shared_fetched_paths: set[str] | None = None,
) -> tuple[str, dict[str, str]]:
    """Build a review prompt for a single batch of files.

    Returns (prompt_text, coverage) where coverage maps file path to
    review mode: "full", "diff-only", or "skipped".

    *shared_fetched_paths* is an optional ``ThreadSafeSet`` (or plain set for
    single-threaded use) used to avoid re-fetching the same sibling/import
    files across batches.
    """
    all_files_lines = []
    for f in all_files:
        if f.get("status") == "removed":
            continue
        all_files_lines.append(
            f"- {f['filename']} ({f.get('status', 'modified')}, "
            f"+{f.get('additions', 0)}/-{f.get('deletions', 0)})"
        )
    all_files_list = "\n".join(all_files_lines)

    batch_files_list = "\n".join(f"- {f['filename']}" for f in batch_files)

    included_files: list[str] = []
    file_contents: dict[str, str] = {}
    coverage: dict[str, str] = {}
    total_chars = 0
    max_chars = 120_000

    for f in batch_files:
        path = f["filename"]
        patch = f.get("patch", "")
        status = f.get("status", "modified")
        changes = f.get("changes", 0)

        if changes > 800:
            if patch:
                block = _build_diff_only_block(path, status, patch)
                if total_chars + len(block) <= max_chars:
                    included_files.append(block)
                    total_chars += len(block)
                    coverage[path] = "diff-only"
                    print(f"    [batch {batch_num}] {path}: diff only ({changes} changes)", file=sys.stderr)
                else:
                    coverage[path] = "skipped"
                    print(f"    [batch {batch_num}] {path}: skipped ({changes} changes, exceeded budget)", file=sys.stderr)
                continue
            coverage[path] = "skipped"
            print(f"    [batch {batch_num}] {path}: skipped ({changes} changes, no patch)", file=sys.stderr)
            continue

        content = get_file_content(owner, repo, head_sha, path, token)
        if not content:
            if patch:
                block = _build_diff_only_block(path, status, patch)
                if total_chars + len(block) <= max_chars:
                    included_files.append(block)
                    total_chars += len(block)
                    coverage[path] = "diff-only"
                    print(f"    [batch {batch_num}] {path}: diff only (could not fetch content)", file=sys.stderr)
                else:
                    coverage[path] = "skipped"
                    print(f"    [batch {batch_num}] {path}: skipped (exceeded budget)", file=sys.stderr)
                continue
            coverage[path] = "skipped"
            continue

        file_contents[path] = content
        block = _build_smart_file_block(path, status, content, patch)

        if total_chars + len(block) > max_chars:
            if patch:
                fallback = _build_diff_only_block(path, status, patch)
                if total_chars + len(fallback) <= max_chars:
                    included_files.append(fallback)
                    total_chars += len(fallback)
                    coverage[path] = "diff-only"
                    print(f"    [batch {batch_num}] {path}: diff only (exceeded budget)", file=sys.stderr)
                    continue
            coverage[path] = "skipped"
            print(f"    [batch {batch_num}] {path}: skipped (exceeded budget)", file=sys.stderr)
            continue

        included_files.append(block)
        total_chars += len(block)
        coverage[path] = "full"

    files_text = "\n".join(included_files) if included_files else "(No reviewable file content.)"

    fetched_paths = shared_fetched_paths if shared_fetched_paths is not None else set()

    # Skip sibling/import context for test-only batches — tests are best
    # reviewed against the source they test (already in the PR or related context).
    batch_all_tests = all(
        _is_test_file(f["filename"]) for f in batch_files if f.get("status") != "removed"
    )

    sibling_context = ""
    if tree_paths and not batch_all_tests:
        sibling_context = get_sibling_files(
            batch_files, tree_paths, owner, repo, head_sha, token, fetched_paths,
        )
    sibling_section = (
        "## Sibling files (same directories as changed files)\n\n"
        "These files are NOT being changed but exist alongside the changed files. "
        "Use them to verify the changes follow existing patterns and conventions.\n\n"
        f"{sibling_context}"
    ) if sibling_context else ""

    import_context = ""
    if tree_paths and file_contents and not batch_all_tests:
        changed_paths = {f["filename"] for f in batch_files}
        import_context = get_imported_files(
            file_contents, tree_paths, changed_paths,
            owner, repo, head_sha, token, fetched_paths,
        )
    import_section = (
        "## Imported local modules (referenced by changed code)\n\n"
        "These local project files are imported by the changed files. "
        "Use them to verify correct API usage and interface compliance.\n\n"
        f"{import_context}"
    ) if import_context else ""

    related_context = get_related_context(
        batch_files, owner, repo, head_sha, token, tree_paths, fetched_paths,
    )
    related_context_section = (
        "## Related context files (not part of this PR)\n\n"
        "These files are NOT being changed but may be affected by or relevant "
        "to the changes above.\n\n"
        f"{related_context}"
    ) if related_context else ""

    prompt = BATCH_REVIEW_PROMPT.format(
        title=pr_info.get("title", ""),
        description=(pr_info.get("body") or "(no description)")[:2000],
        all_files_list=all_files_list,
        batch_files_list=batch_files_list,
        batch_num=batch_num,
        total_batches=total_batches,
        repo_context_section=shared_context["repo_context_section"],
        repo_docs_section=shared_context["repo_docs_section"],
        guidelines_section=shared_context["guidelines_section"],
        linter_config_section=shared_context.get("linter_config_section", ""),
        repo_tree_section=shared_context["repo_tree_section"],
        sibling_section=sibling_section,
        import_section=import_section,
        related_context_section=related_context_section,
        files_text=files_text,
    )
    return prompt, coverage


def build_reduce_prompt(
    pr_info: dict,
    files: list[dict],
    batch_results: list[dict],
    guidelines: str = "",
    linter_config_section: str = "",
) -> str:
    """Build the reduce-phase prompt from batch results and all file diffs."""
    max_results_chars = 120_000
    results_parts: list[str] = []
    results_chars = 0
    for i, result in enumerate(batch_results, 1):
        summary = result.get("summary", {})
        comments = result.get("comments", [])
        part = f"### Batch {i} summary\n"
        part += f"Overview: {summary.get('overview', 'N/A')}\n"
        changes = summary.get("changes", [])
        if changes:
            part += "Changes:\n" + "\n".join(f"- {c}" for c in changes) + "\n"
        files_desc = summary.get("files", [])
        if files_desc:
            part += "Files:\n" + "\n".join(
                f"- `{fd.get('path', '')}`: {fd.get('description', '')}"
                for fd in files_desc
            ) + "\n"
        comments_json = json.dumps(comments, indent=2)
        if results_chars + len(part) + len(comments_json) > max_results_chars:
            comments_json = json.dumps(
                [{"path": c.get("path"), "line": c.get("line"),
                  "severity": c.get("severity"), "message": c.get("message")}
                 for c in comments if isinstance(c, dict)],
            )
        part += f"\n### Batch {i} comments ({len(comments)} total)\n"
        part += "```json\n" + comments_json + "\n```\n"
        if results_chars + len(part) > max_results_chars:
            results_parts.append(
                f"... (batch {i}–{len(batch_results)} truncated to fit budget)\n"
            )
            break
        results_parts.append(part)
        results_chars += len(part)

    batch_results_text = "\n".join(results_parts) if results_parts else "(No batch results.)"

    commented_paths: set[str] = set()
    for result in batch_results:
        for c in result.get("comments", []):
            if isinstance(c, dict) and c.get("path"):
                commented_paths.add(c["path"])

    # Full diffs only for files that received comments (need them for validation).
    # Compact one-line summaries for the rest (reduce phase already has batch
    # summaries describing what each file does).
    diffs: list[str] = []
    other_summaries: list[str] = []
    total_chars = 0

    reviewable = [f for f in files if f.get("status") != "removed"]
    for f in reviewable:
        path = f["filename"]
        patch = f.get("patch", "")
        status = f.get("status", "modified")
        adds = f.get("additions", 0)
        dels = f.get("deletions", 0)

        if path in commented_paths and patch:
            block = f"FILE: {path} (status: {status})\nDIFF:\n{patch}\n"
            if total_chars + len(block) > REDUCE_MAX_DIFFS_CHARS:
                diffs.append("... (remaining commented-file diffs truncated)")
                break
            diffs.append(block)
            total_chars += len(block)
        else:
            other_summaries.append(f"- {path} ({status}, +{adds}/-{dels})")

    all_diffs_text = "\n".join(diffs) if diffs else "(no commented-file diffs)"
    if other_summaries:
        all_diffs_text += (
            "\n\n### Other changed files (no comments — summary only)\n"
            + "\n".join(other_summaries)
        )

    guidelines_section = (
        f"## Repository guidelines\n\n{guidelines}" if guidelines else ""
    )

    return REDUCE_REVIEW_PROMPT.format(
        title=pr_info.get("title", ""),
        description=(pr_info.get("body") or "(no description)")[:2000],
        guidelines_section=guidelines_section,
        linter_config_section=linter_config_section,
        batch_results_text=batch_results_text,
        all_diffs_text=all_diffs_text,
    )


